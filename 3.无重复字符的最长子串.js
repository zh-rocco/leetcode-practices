// @lc code=start
/**
 * @param {string} s
 * @return {number}
 */
var lengthOfLongestSubstring = function (s) {
  let p1 = 0
  let p2 = 0
  let max = 0
  let map = {}
  const len = s.length

  for (const v of s) {
    if (map.hasOwnProperty(v)) {
      max = Math.max(max, p2 - p1)
      p1 = Math.max(map[v] + 1, p1)
    }

    map[v] = p2
    p2++
  }

  return Math.max(max, p2 - p1)
};
